package day03_comments_escape_sequence;

public class PrintVsPrintln {

    public static void main(String[] args) {

//             NOT: println() vs print()
//    System.out.println();
//      prints given to console. Then goes to the next line
//    System.out.print();
//      prints given to the console, but all in the same line
//    System.out.print();
//       cannot be used without input


        System.out.println("Apples");
        System.out.println("Pears");

        System.out.print("Bananas, ");
        System.out.print("Oranges");

        System.out.println();
        System.out.println("Pineapples");

    }
}
