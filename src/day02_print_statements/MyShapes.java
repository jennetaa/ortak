package day02_print_statements;

public class MyShapes {

    public static void main(String[] args) {

        System.out.println("    *  ");
        System.out.println("   *** ");
        System.out.println("  *****  ");


        System.out.println();


        System.out.println("0000000000");
        System.out.println("//////////");
        System.out.println("||||||||||");
        System.out.println("&&&&&&&&&&&");
    }
}
